a = float(input("первое число"))
b = float(input("второе число"))
c = input('какое мат действие нужно сделать?')
if c == '+':
    print(a + b)
elif c == '-':
    print(a - b)
elif c == '*':
    print(a * b)
elif c == '/':
    print(a / b)



